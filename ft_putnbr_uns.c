#include"printf.h"

char ft_putnbr_uns(unsigned int n)
{
	if (n == 0)
	{
		write(1, "0", 1);
		return (1);
	}
	if (n > 9)
	{
		ft_putnbr(n / 10);
		ft_putchar((n % 10) + '0');	
	}
	else
	{
		ft_putchar(n + '0');
	}
	return(1);
}